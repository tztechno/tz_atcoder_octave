A = input('', 's');
A = strtrim(A);

DP = [0, 0, 0, 0];
ans = 0;

for i = 1:3
    if A(i) == 'R'
        DP(i + 1) = DP(i) + 1;
        if DP(i + 1) > ans
            ans = DP(i + 1);
        end
    end
end

disp(ans);

